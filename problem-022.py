#Demonstrating Assignment Operators
a=10
print("initial value:",a)
a+=5
print("After (a+=5) =",a)
a-=6
print("After (a-=6) =",a)
a*=7
print("After (a*=7) =",a)
a/=8
print("After (a/=8) =",a)

